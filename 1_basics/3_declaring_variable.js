/**
 * Variable 선언하기
 *
 * 1) var - 잘 안써
 * 2) let
 * 3) const
 */

var name = '됴파';
console.log(name);

var age = 28;
console.log(age);

let ive = '아이브';
console.log(ive);

/**
 * let과 var로 선언하면
 * 값을 추후 변경 가능
 */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

